// 춘천 시내 고정 그리드 16점 · tokens.map.center [127.735, 37.885] 주변 // DEMO 좌표
const GRID = [
  [127.712, 37.9],
  [127.748, 37.897],
  [127.702, 37.879],
  [127.76, 37.874],
  [127.727, 37.906],
  [127.741, 37.866],
  [127.716, 37.862],
  [127.755, 37.889],
  [127.707, 37.891],
  [127.734, 37.912],
  [127.763, 37.902],
  [127.722, 37.87],
  [127.746, 37.881],
  [127.7, 37.9],
  [127.757, 37.86],
  [127.731, 37.858],
];

// 단순 문자열 해시(djb2 계열) · 결정적
const hash = (s) => {
  let h = 5381;
  for (let i = 0; i < s.length; i += 1) h = (h * 33 + s.charCodeAt(i)) | 0;
  return Math.abs(h);
};

// venue → 항상 유효한 [lng, lat] · 실좌표 우선, 목업은 해시 슬롯 + 해시 파생 지터(슬롯 충돌 시각 분리)
export function venueCoord(venue) {
  if (venue.coord) return venue.coord;
  const h = hash(venue.id);
  const [lng, lat] = GRID[h % GRID.length];
  const j = (((h >> 4) % 7) - 3) * 0.0012; // DEMO 좌표 · 결정적 지터 ±0.0036
  return [lng + j, lat - j / 2];
}

export default venueCoord;
